                <p>Infak adalah harta yang dikeluarkan oleh seseorang atau badan usaha di luar zakat untuk kemaslahatan umum (Menurut Undang-Undang Nomor 23 Tahun 2011 tentang Pengelolaan Zakat pada BAB I Pasal 1). Infak merupakan amalan yang tak bisa lepas dari kehidupan sehari-hari seorang Muslim. Infak berasal dari Bahasa Arab, "anfaqa" yang berarti membelanjakan harta atau memberikan harta. Sedangkan infak berarti keluarkanlah harta.</p>
                <p>Sejatinya infak dibagi menjadi dua, ada infak untuk kebaikan, dan infak untuk keburukan. Infak kebaikan ini dilakukan atau dibelanjakan untuk di jalan Allah, yang juga dengan harta berasal dari hal baik.</p>
                <p>Sedangkan infak keburukan contohnya, dijelaskan dalam Surat Al-Anfal Ayat 36, yang artinya sebagai berikut: "Sesungguhnya orang-orang yang kafir menafkahkan harta mereka untuk menghalangi (orang) dari jalan Allah. Mereka akan menafkahkan harta itu, kemudian menjadi sesalan bagi mereka, dan mereka akan dikalahkan. Dan ke dalam Jahannamlah orang-orang yang kafir itu dikumpulkan" (QS. Al-Anfal : 36).</p>
                <p>Allah Subhanahu Wata’ala memerintahkan setiap hambanya agar menyisihkan hartanya untuk berinfak yang hal ini masuk dalam kebaikan, dan Allah mencintai hambanya yang berbuat baik. Hal ini dijelaskan dalam Surat Ali Imran ayat 133-134.</p>
                <p>“Dan bersegeralah kamu kepada keampunan Tuhanmu dan kepada surga yang luasnya seluas langit dan bumi yang disediakan untuk orang-orang yang takwa. Yaitu orang-orang yang menginfakkan (hartanya) baik di waktu senang atau di waktu susah, dan orang-orang yang menahan kemarahannya dan memaafkan kesalahan orang. Allah mencintai orang-orang yang berbuat kebaikan”. (QS. Ali Imran: 133-134).</p>
                <p>Infak ternyata memiliki perbedaan dari sedekah, infak sebenarnya dilakukan dengan harta atau material, sedangkan sedekah, bisa dilakukan dengan non-harta atau non-material. Misalnya saja sedekah bisa dilakukan dengan senyuman, “Senyummu terhadap wajah saudaramu adalah sedekah.” (HR. Tirmidzi).</p>
                <p><strong>Keutamaan Berinfak:</strong></p>
                <p>1. Memperoleh Pahala yang Besar</p>
                <p>“Berimanlah kamu kepada Allah dan Rasul-Nya dan infakkanlah sebahagian dari hartamu yang Allah menjadikan kamu menguasainya. Maka orang-orang yang beriman di antara kamu dan menginfakkan (sebahagian) dari hartanya memperolehi pahala yang besar”. (QS. Al-Hadid: 7).</p>
                <p>2. Didoakan Malaikat</p>
                <p>“Ketika hamba berada di setiap pagi, ada dua malaikat yang turun dan berdoa, “Ya Allah berikanlah ganti pada yang gemar berinfak (rajin memberi nafkah pada keluarga).” Malaikat yang lain berdoa, “Ya Allah, berikanlah kebangkrutan bagi yang enggan bersedekah (memberi nafkah).” (HR. Bukhari).</p>
                <p>3. Allah Ganti Harta yang Diinfakkan</p>
                <p>"Katakanlah: 'Sesungguhnya Tuhanku melapangkan rezeki bagi siapa yang dikehendaki-Nya di antara hamba-hamba-Nya dan menyempitkan bagi (siapa yang dikehendaki-Nya)'. Dan barang apa saja yang kamu nafkahkan (belanjakan), maka Allah akan menggantinya dan Dialah Pemberi rezeki yang sebaik-baiknya." (QS. Saba: 39).</p>
